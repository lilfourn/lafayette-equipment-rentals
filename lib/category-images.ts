// Function to get category image with fallback
export function getCategoryImage(
  categoryName: string | undefined,
  displayName: string | undefined
): string | null {
  if (!categoryName && !displayName) return null;

  // Try exact match with category name
  if (categoryName) {
    const normalizedName = categoryName.toLowerCase().trim();
    if (categoryImageMap[normalizedName]) {
      return categoryImageMap[normalizedName];
    }
  }

  // Try exact match with display name
  if (displayName) {
    const normalizedDisplay = displayName.toLowerCase().trim();
    if (categoryImageMap[normalizedDisplay]) {
      return categoryImageMap[normalizedDisplay];
    }
  }

  // Try partial match with keywords
  const searchTerms = [categoryName, displayName]
    .filter(Boolean)
    .join(" ")
    .toLowerCase();

  // Check for specific equipment types
  if (searchTerms.includes("pump") || searchTerms.includes("tank")) {
    return "/category images/water_pump.png";
  }
  if (searchTerms.includes("trench") || searchTerms.includes("bore")) {
    return "/category images/trencher.png";
  }
  if (
    searchTerms.includes("traffic") ||
    searchTerms.includes("safety") ||
    searchTerms.includes("road safety")
  ) {
    return "/category images/message_board.png";
  }
  if (searchTerms.includes("storage") || searchTerms.includes("container")) {
    return "/category images/storage_container.png";
  }
  if (searchTerms.includes("screen") || searchTerms.includes("pulveriz")) {
    return "/category images/screener.png";
  }
  if (searchTerms.includes("crush")) {
    return "/category images/jaw_crusher.png";
  }
  if (
    searchTerms.includes("pav") ||
    searchTerms.includes("asphalt") ||
    searchTerms.includes("road surface")
  ) {
    return "/category images/paver.png";
  }
  if (searchTerms.includes("clear") || searchTerms.includes("mulch")) {
    return "/category images/forestry_mulcher.png";
  }
  if (
    searchTerms.includes("lift") ||
    searchTerms.includes("aerial") ||
    searchTerms.includes("elevated")
  ) {
    return "/category images/scissor_lift.png";
  }
  if (searchTerms.includes("excavat") || searchTerms.includes("dig")) {
    return "/category images/excavator.png";
  }
  if (searchTerms.includes("compact") || searchTerms.includes("roller")) {
    return "/category images/compactor.png";
  }
  if (searchTerms.includes("truck")) {
    return "/category images/dump_truck.png";
  }
  if (searchTerms.includes("trailer")) {
    return "/category images/trailer.png";
  }
  if (searchTerms.includes("concrete") || searchTerms.includes("cement")) {
    return "/category images/concrete_mixer.png";
  }
  if (searchTerms.includes("forest") || searchTerms.includes("tree")) {
    return "/category images/forestry_mulcher.png";
  }
  if (
    searchTerms.includes("mow") ||
    searchTerms.includes("grass") ||
    searchTerms.includes("landscape")
  ) {
    return "/category images/mower.png";
  }
  if (searchTerms.includes("generat") || searchTerms.includes("power")) {
    return "/category images/generator.png";
  }
  if (searchTerms.includes("light") || searchTerms.includes("illuminat")) {
    return "/category images/light_tower.png";
  }
  if (searchTerms.includes("fork") || searchTerms.includes("material")) {
    return "/category images/forklift.png";
  }
  if (searchTerms.includes("skid")) {
    return "/category images/skid_steer.png";
  }
  if (searchTerms.includes("load")) {
    return "/category images/wheel_loader.png";
  }
  if (searchTerms.includes("doz")) {
    return "/category images/dozer.png";
  }

  return null;
}
